public class MiceDie {
    double size, angle;

    public MiceDie(double s, double a){
        size=s;
        angle=a;

    }

    public double getSize(){
        return size;
    }

    public void setSize(double s){
        size=s;
    }

    public double getAngle(){
        return angle;
    }

    public void setAngle(double a){
        angle=a;
    }
}
